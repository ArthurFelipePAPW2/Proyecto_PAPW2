    {
        
        $descripcion = $request->get('descripcion');
        $admin = $request->get('admin');

        $curiosidad = new diduknow(array(
                        "description" => $descripcion,
                        "id-administrador" => $admin
        ));

        $curiosidad->save();

        return redirect('/admin');
    }

